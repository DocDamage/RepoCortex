function Write-GitHookLog {
    <#
    .SYNOPSIS
        Writes a structured log entry for Git hook operations.

    .DESCRIPTION
        Internal function for logging hook events using the structured logging system.

    .PARAMETER HookType
        The type of hook.

    .PARAMETER Action
        The action performed (Install, Uninstall, Execute, etc.).

    .PARAMETER Status
        The status of the action.

    .PARAMETER ProjectRoot
        Path to the project root.

    .PARAMETER Details
        Additional details about the operation.
    #>
    [CmdletBinding()]
    param(
        [Parameter(Mandatory = $true)]
        [string]$HookType,

        [Parameter(Mandatory = $true)]
        [string]$Action,

        [Parameter(Mandatory = $true)]
        [string]$Status,

        [Parameter()]
        [string]$ProjectRoot = '.',

        [Parameter()]
        [string]$Details = ''
    )

    try {
        # Check if logging function is available
        $logCmd = Get-Command New-LogEntry -ErrorAction SilentlyContinue
        if (-not $logCmd) {
            # Fall back to verbose output
            Write-Verbose "[GitHook] $HookType - $Action : $Status"
            return
        }

        $metadata = @{
            hookType = $HookType
            action = $Action
            projectRoot = $ProjectRoot
            details = $Details
        }

        $entry = New-LogEntry -Level 'INFO' -Message "Git hook $Action : $Status" `
            -Source 'GitHooks' -Metadata $metadata

        # Try to write structured log
        $writeCmd = Get-Command Write-StructuredLog -ErrorAction SilentlyContinue
        if ($writeCmd) {
            $entry | Write-StructuredLog -Force
        }
    } catch {
        # Silent failure for logging
        Write-Verbose "[GitHook] Failed to write log: $_"
    }
}

