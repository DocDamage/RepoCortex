$script:ValidHookTypes = @('pre-commit', 'post-commit', 'pre-push', 'post-merge')
$script:HookScriptHeader = @'
#!/usr/bin/env pwsh
# LLM Workflow Git Hook
# Auto-generated by LLMWorkflow - DO NOT EDIT
# Generated: {0}

$ErrorActionPreference = 'Stop'

# Ensure module is available
try {
    Import-Module LLMWorkflow -ErrorAction Stop
} catch {
    Write-Error "Failed to import LLMWorkflow module: $_"
    exit 1
}

# Run hook logic
'@

$script:HookScriptFooter = @'
exit $result.ExitCode
'@

