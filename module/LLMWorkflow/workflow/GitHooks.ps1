function Install-LLMWorkflowGitHooks {
    <#
    .SYNOPSIS
        Installs LLM Workflow Git hooks into .git/hooks.

    .DESCRIPTION
        Installs the specified Git hooks for the LLM Workflow platform.
        Supports pre-commit (secret scanning, health check), post-commit (auto-sync),
        pre-push (validation, compatibility check), and post-merge (rebuild packs).
        Optionally backs up existing hooks before overwriting.

    .PARAMETER ProjectRoot
        Path to the project root containing the .git directory. Defaults to current directory.

    .PARAMETER Hooks
        Array of hook types to install: pre-commit, post-commit, pre-push, post-merge.
        Defaults to all hooks.

    .PARAMETER BackupExisting
        If specified, existing hooks will be backed up before being overwritten.

    .PARAMETER Global
        If specified, installs hooks to the global Git hooksPath instead of local .git/hooks.

    .EXAMPLE
        Install-LLMWorkflowGitHooks
        Installs all hooks to the current project's .git/hooks directory.

    .EXAMPLE
        Install-LLMWorkflowGitHooks -Hooks @('pre-commit', 'pre-push') -BackupExisting
        Installs only pre-commit and pre-push hooks, backing up any existing ones.

    .OUTPUTS
        PSCustomObject with installation results.
    #>
    [CmdletBinding(SupportsShouldProcess = $true)]
    param(
        [Parameter()]
        [string]$ProjectRoot = '.',

        [Parameter()]
        [ValidateSet('pre-commit', 'post-commit', 'pre-push', 'post-merge')]
        [string[]]$Hooks = $script:ValidHookTypes,

        [Parameter()]
        [switch]$BackupExisting,

        [Parameter()]
        [switch]$Global
    )

    # Resolve project root
    $resolvedRoot = Resolve-Path -LiteralPath $ProjectRoot -ErrorAction SilentlyContinue
    if (-not $resolvedRoot) {
        throw "Project root does not exist: $ProjectRoot"
    }
    $resolvedRoot = $resolvedRoot.Path

    # Determine hooks directory
    $hooksDir = if ($Global) {
        $globalHooksPath = & git config --global core.hooksPath 2>$null
        if ($globalHooksPath) {
            $globalHooksPath
        } else {
            $defaultGlobalDir = if ($IsWindows -or ($PSVersionTable.PSVersion.Major -lt 6)) {
                Join-Path $HOME '.git-hooks'
            } else {
                Join-Path $HOME '.config/git/hooks'
            }
            # Ensure global hooks directory exists and configure Git
            if (-not (Test-Path -LiteralPath $defaultGlobalDir)) {
                New-Item -ItemType Directory -Path $defaultGlobalDir -Force | Out-Null
            }
            & git config --global core.hooksPath $defaultGlobalDir
            $defaultGlobalDir
        }
    } else {
        $gitDir = Join-Path $resolvedRoot '.git'
        if (-not (Test-Path -LiteralPath $gitDir)) {
            throw "No .git directory found in $resolvedRoot. Initialize a Git repository first."
        }
        Join-Path $gitDir 'hooks'
    }

    # Ensure hooks directory exists
    if (-not (Test-Path -LiteralPath $hooksDir)) {
        New-Item -ItemType Directory -Path $hooksDir -Force | Out-Null
    }

    $results = @{
        Installed = @()
        BackedUp = @()
        Skipped = @()
        Failed = @()
    }

    foreach ($hookType in $Hooks) {
        $hookPath = Join-Path $hooksDir $hookType
        $backupPath = "$hookPath.backup.$(Get-Date -Format 'yyyyMMdd_HHmmss')"

        # Check for existing hook
        if (Test-Path -LiteralPath $hookPath) {
            if ($BackupExisting) {
                if ($PSCmdlet.ShouldProcess("Backup $hookType hook", "Backup")) {
                    try {
                        Copy-Item -LiteralPath $hookPath -Destination $backupPath -Force
                        $results.BackedUp += @{
                            HookType = $hookType
                            BackupPath = $backupPath
                        }
                    } catch {
                        $results.Failed += @{
                            HookType = $hookType
                            Reason = "Failed to backup existing hook: $_"
                        }
                        continue
                    }
                }
            }
        }

        if ($PSCmdlet.ShouldProcess("Install $hookType hook to $hookPath", "Install")) {
            try {
                # Generate hook script content
                $hookContent = New-GitHookScript -HookType $hookType -ProjectRoot $resolvedRoot

                # Write hook script
                $hookContent | Out-File -LiteralPath $hookPath -Encoding UTF8 -Force

                # Set executable permissions on Unix-like systems
                if (-not ($IsWindows -or ($PSVersionTable.PSVersion.Major -lt 6))) {
                    chmod +x $hookPath
                }

                $results.Installed += $hookType

                # Log installation
                Write-GitHookLog -HookType $hookType -Action 'Install' -Status 'Success' -ProjectRoot $resolvedRoot
            } catch {
                $results.Failed += @{
                    HookType = $hookType
                    Reason = $_.Exception.Message
                }
            }
        } else {
            $results.Skipped += $hookType
        }
    }

    # Write summary
    $summary = [PSCustomObject]@{
        ProjectRoot = $resolvedRoot
        HooksDirectory = $hooksDir
        IsGlobal = $Global.IsPresent
        InstalledCount = $results.Installed.Count
        BackedUpCount = $results.BackedUp.Count
        FailedCount = $results.Failed.Count
        Installed = $results.Installed
        BackedUp = $results.BackedUp
        Failed = $results.Failed
    }

    return $summary
}

function Uninstall-LLMWorkflowGitHooks {
    <#
    .SYNOPSIS
        Removes installed LLM Workflow Git hooks.

    .DESCRIPTION
        Uninstalls the specified Git hooks from the .git/hooks directory.
        Optionally restores from backup if available.

    .PARAMETER ProjectRoot
        Path to the project root containing the .git directory. Defaults to current directory.

    .PARAMETER Hooks
        Array of hook types to uninstall. Defaults to all hooks.

    .PARAMETER RestoreBackup
        If specified and a backup exists, restores the backup instead of removing.

    .PARAMETER KeepBackup
        If specified alongside RestoreBackup, keeps the backup file after restoration.

    .EXAMPLE
        Uninstall-LLMWorkflowGitHooks
        Removes all LLM Workflow hooks from the current project.

    .EXAMPLE
        Uninstall-LLMWorkflowGitHooks -Hooks @('pre-commit') -RestoreBackup
        Restores the pre-commit hook from its most recent backup.

    .OUTPUTS
        PSCustomObject with uninstallation results.
    #>
    [CmdletBinding(SupportsShouldProcess = $true)]
    param(
        [Parameter()]
        [string]$ProjectRoot = '.',

        [Parameter()]
        [ValidateSet('pre-commit', 'post-commit', 'pre-push', 'post-merge')]
        [string[]]$Hooks = $script:ValidHookTypes,

        [Parameter()]
        [switch]$RestoreBackup,

        [Parameter()]
        [switch]$KeepBackup
    )

    # Resolve project root
    $resolvedRoot = Resolve-Path -LiteralPath $ProjectRoot -ErrorAction SilentlyContinue
    if (-not $resolvedRoot) {
        throw "Project root does not exist: $ProjectRoot"
    }
    $resolvedRoot = $resolvedRoot.Path

    $hooksDir = Join-Path $resolvedRoot '.git' 'hooks'
    if (-not (Test-Path -LiteralPath $hooksDir)) {
        throw "No .git/hooks directory found in $resolvedRoot"
    }

    $results = @{
        Removed = @()
        Restored = @()
        NotFound = @()
        Failed = @()
    }

    foreach ($hookType in $Hooks) {
        $hookPath = Join-Path $hooksDir $hookType

        if (-not (Test-Path -LiteralPath $hookPath)) {
            $results.NotFound += $hookType
            continue
        }

        # Check if it's an LLM Workflow hook
        $content = Get-Content -LiteralPath $hookPath -Raw -ErrorAction SilentlyContinue
        $isLLMWorkflowHook = $content -and ($content -match 'LLM Workflow Git Hook')

        if (-not $isLLMWorkflowHook) {
            Write-Warning "Hook $hookType does not appear to be an LLM Workflow hook. Skipping."
            $results.NotFound += $hookType
            continue
        }

        if ($RestoreBackup) {
            # Find most recent backup
            $backupPattern = "$hookPath.backup.*"
            $backups = Get-ChildItem -Path $backupPattern -ErrorAction SilentlyContinue | 
                Sort-Object LastWriteTime -Descending

            if ($backups.Count -gt 0) {
                $mostRecentBackup = $backups[0].FullName
                if ($PSCmdlet.ShouldProcess("Restore $hookType from backup", "Restore")) {
                    try {
                        Move-Item -LiteralPath $mostRecentBackup -Destination $hookPath -Force
                        $results.Restored += @{
                            HookType = $hookType
                            BackupUsed = $mostRecentBackup
                        }

                        if (-not $KeepBackup) {
                            # Remove older backups
                            $backups | Select-Object -Skip 1 | Remove-Item -Force -ErrorAction SilentlyContinue
                        }

                        Write-GitHookLog -HookType $hookType -Action 'Restore' -Status 'Success' -ProjectRoot $resolvedRoot
                    } catch {
                        $results.Failed += @{
                            HookType = $hookType
                            Reason = "Failed to restore backup: $_"
                        }
                    }
                }
            } else {
                Write-Warning "No backup found for $hookType hook."
                $results.Failed += @{
                    HookType = $hookType
                    Reason = "No backup found"
                }
            }
        } else {
            if ($PSCmdlet.ShouldProcess("Remove $hookType hook", "Remove")) {
                try {
                    Remove-Item -LiteralPath $hookPath -Force
                    $results.Removed += $hookType

                    Write-GitHookLog -HookType $hookType -Action 'Uninstall' -Status 'Success' -ProjectRoot $resolvedRoot
                } catch {
                    $results.Failed += @{
                        HookType = $hookType
                        Reason = $_.Exception.Message
                    }
                }
            }
        }
    }

    return [PSCustomObject]@{
        ProjectRoot = $resolvedRoot
        RemovedCount = $results.Removed.Count
        RestoredCount = $results.Restored.Count
        FailedCount = $results.Failed.Count
        Removed = $results.Removed
        Restored = $results.Restored
        NotFound = $results.NotFound
        Failed = $results.Failed
    }
}

