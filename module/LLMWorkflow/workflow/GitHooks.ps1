function Invoke-GitHookPreCommit {
    <#
    .SYNOPSIS
        Executes pre-commit hook logic.

    .DESCRIPTION
        Performs secret scanning, health score check, and policy validation.
        Fails the commit if critical issues are found.

    .PARAMETER ProjectRoot
        Path to the project root. Defaults to current directory.

    .PARAMETER StagedFilesOnly
        If specified, only scans staged files. Otherwise scans all modified files.

    .PARAMETER SkipSecretScan
        If specified, skips secret scanning (not recommended).

    .PARAMETER SkipHealthCheck
        If specified, skips health check.

    .PARAMETER SeverityThreshold
        Minimum severity to block commit: low, medium, high, critical.
        Defaults to critical.

    .EXAMPLE
        $result = Invoke-GitHookPreCommit
        Runs pre-commit checks and returns result object.

    .OUTPUTS
        PSCustomObject with ExitCode (0=OK, 1=Block) and check results.
    #>
    [CmdletBinding()]
    param(
        [Parameter()]
        [string]$ProjectRoot = '.',

        [Parameter()]
        [switch]$StagedFilesOnly,

        [Parameter()]
        [switch]$SkipSecretScan,

        [Parameter()]
        [switch]$SkipHealthCheck,

        [Parameter()]
        [ValidateSet('low', 'medium', 'high', 'critical')]
        [string]$SeverityThreshold = 'critical'
    )

    $startTime = Get-Date
    $exitCode = 0
    $findings = @()
    $messages = @()

    # Get current execution mode
    $executionMode = Get-CurrentExecutionMode
    $isCI = ($executionMode -eq 'ci') -or ($env:CI -eq 'true')

    try {
        # Get files to scan
        $filesToScan = @()
        if ($StagedFilesOnly) {
            $stagedOutput = & git diff --cached --name-only --diff-filter=ACM 2>$null
            if ($stagedOutput) {
                $filesToScan = $stagedOutput -split "`n" | Where-Object { $_ }
            }
        } else {
            $modifiedOutput = & git diff --name-only --diff-filter=ACM 2>$null
            if ($modifiedOutput) {
                $filesToScan = $modifiedOutput -split "`n" | Where-Object { $_ }
            }
        }

        # Secret Scanning
        if (-not $SkipSecretScan) {
            Write-Verbose "[PreCommit] Scanning for secrets..."
            $secretFindings = @()

            foreach ($file in $filesToScan) {
                $filePath = Join-Path $ProjectRoot $file
                if (Test-Path -LiteralPath $filePath -PathType Leaf) {
                    try {
                        $content = Get-Content -LiteralPath $filePath -Raw -ErrorAction SilentlyContinue
                        if ($content) {
                            $scanResult = Test-SecretInContent -Content $content -SeverityThreshold $SeverityThreshold
                            if ($scanResult.HasSecrets) {
                                $criticalCount = ($scanResult.Findings | Where-Object { $_.Severity -eq 'critical' }).Count
                                $secretFindings += @{
                                    File = $file
                                    Findings = $scanResult.Findings
                                    CriticalCount = $criticalCount
                                }
                            }
                        }
                    } catch {
                        Write-Verbose "[PreCommit] Failed to scan file $file : $_"
                    }
                }
            }

            if ($secretFindings.Count -gt 0) {
                $totalCritical = ($secretFindings | Measure-Object -Property CriticalCount -Sum).Sum
                if ($totalCritical -gt 0) {
                    $exitCode = 1
                    $messages += "CRITICAL: Found $totalCritical critical secrets in staged files"
                }
                $findings += @{
                    Check = 'SecretScan'
                    Status = if ($totalCritical -gt 0) { 'Failed' } else { 'Warning' }
                    Details = $secretFindings
                }
            } else {
                $findings += @{
                    Check = 'SecretScan'
                    Status = 'Passed'
                    Details = $null
                }
            }
        }

        # Health Check
        if (-not $SkipHealthCheck -and $exitCode -eq 0) {
            Write-Verbose "[PreCommit] Running health check..."
            # Quick health check - verify critical components
            $healthIssues = @()

            # Check .env file exists and has required keys
            $envPath = Join-Path $ProjectRoot '.env'
            if (-not (Test-Path -LiteralPath $envPath)) {
                $healthIssues += "Missing .env file"
            }

            # Check for pack manifest validity
            $manifestDir = Join-Path $ProjectRoot 'packs' 'manifests'
            if (Test-Path -LiteralPath $manifestDir) {
                $manifests = Get-ChildItem -Path $manifestDir -Filter '*.json' -ErrorAction SilentlyContinue
                foreach ($manifest in $manifests) {
                    try {
                        $content = Get-Content -LiteralPath $manifest.FullName -Raw | ConvertFrom-Json
                        # Basic validation
                        if (-not $content.packId -or -not $content.version) {
                            $healthIssues += "Invalid manifest: $($manifest.Name)"
                        }
                    } catch {
                        $healthIssues += "Corrupted manifest: $($manifest.Name)"
                    }
                }
            }

            if ($healthIssues.Count -gt 0) {
                $findings += @{
                    Check = 'HealthCheck'
                    Status = 'Warning'
                    Details = $healthIssues
                }
                $messages += "Health check warnings: $($healthIssues -join ', ')"
            } else {
                $findings += @{
                    Check = 'HealthCheck'
                    Status = 'Passed'
                    Details = $null
                }
            }
        }

        # Policy Validation
        $policy = Get-PolicyRules
        if ($policy -and $policy.rules) {
            $currentMode = Get-CurrentExecutionMode
            # Check if commits are allowed in current mode
            $allowed = Test-PolicyPermission -Command 'commit' -Mode $currentMode -Policy $policy
            if (-not $allowed) {
                $exitCode = 1
                $findings += @{
                    Check = 'Policy'
                    Status = 'Failed'
                    Details = "Commits not allowed in $currentMode mode"
                }
                $messages += "Policy violation: Commits blocked in $currentMode mode"
            } else {
                $findings += @{
                    Check = 'Policy'
                    Status = 'Passed'
                    Details = $null
                }
            }
        }

    } catch {
        $exitCode = 1
        $messages += "Error during pre-commit: $_"
        $findings += @{
            Check = 'Error'
            Status = 'Failed'
            Details = $_.Exception.Message
        }
    }

    $duration = ((Get-Date) - $startTime).TotalSeconds

    # Log the hook execution
    Write-GitHookLog -HookType 'pre-commit' -Action 'Execute' -Status $(if ($exitCode -eq 0) { 'Success' } else { 'Blocked' }) `
        -ProjectRoot $ProjectRoot -Details "Duration: ${duration}s, Files: $($filesToScan.Count)"

    # Output messages in interactive mode
    if (-not $isCI -and $messages.Count -gt 0) {
        foreach ($msg in $messages) {
            if ($msg -match '^CRITICAL') {
                Write-Host $msg -ForegroundColor Red
            } elseif ($msg -match '^Warning') {
                Write-Warning $msg
            } else {
                Write-Host $msg
            }
        }
    }

    return [PSCustomObject]@{
        ExitCode = $exitCode
        Success = ($exitCode -eq 0)
        Duration = $duration
        FilesScanned = $filesToScan.Count
        Findings = $findings
        Messages = $messages
    }
}

function Invoke-GitHookPostCommit {
    <#
    .SYNOPSIS
        Executes post-commit hook logic.

    .DESCRIPTION
        Queues sync for affected sources, updates sync state.
        This hook is non-blocking and always returns success.

    .PARAMETER ProjectRoot
        Path to the project root. Defaults to current directory.

    .PARAMETER SkipSync
        If specified, skips the auto-sync operation.

    .EXAMPLE
        Invoke-GitHookPostCommit
        Runs post-commit operations.

    .OUTPUTS
        PSCustomObject with operation results.
    #>
    [CmdletBinding()]
    param(
        [Parameter()]
        [string]$ProjectRoot = '.',

        [Parameter()]
        [switch]$SkipSync
    )

    $startTime = Get-Date
    $messages = @()
    $syncQueued = $false

    try {
        # Get commit info
        $commitHash = & git rev-parse HEAD 2>$null
        $commitMsg = & git log -1 --pretty=%B 2>$null
        $changedFiles = & git diff-tree --no-commit-id --name-only -r HEAD 2>$null

        if ($changedFiles) {
            $affectedFiles = $changedFiles -split "`n" | Where-Object { $_ }

            # Check if any pack files were modified
            $packFiles = $affectedFiles | Where-Object { $_ -like 'packs/*' -or $_ -like '.memorybridge/*' }

            if ($packFiles.Count -gt 0 -and -not $SkipSync) {
                # Queue sync for affected sources (non-blocking)
                $syncStatePath = Join-Path $ProjectRoot '.memorybridge' 'sync-queue.json'
                $syncEntry = @{
                    timestamp = [DateTime]::UtcNow.ToString("o")
                    commitHash = $commitHash
                    affectedFiles = $packFiles
                    status = 'pending'
                }

                # Ensure directory exists
                $syncDir = Split-Path -Parent $syncStatePath
                if (-not (Test-Path -LiteralPath $syncDir)) {
                    New-Item -ItemType Directory -Path $syncDir -Force | Out-Null
                }

                # Append to queue
                $syncEntry | ConvertTo-Json -Compress | Add-Content -LiteralPath $syncStatePath -ErrorAction SilentlyContinue
                $syncQueued = $true
                $messages += "Queued sync for $($packFiles.Count) pack files"
            }

            # Update sync state if it exists
            $syncStateFile = Join-Path $ProjectRoot '.memorybridge' 'sync-state.json'
            if (Test-Path -LiteralPath $syncStateFile) {
                try {
                    $state = Get-Content -LiteralPath $syncStateFile -Raw | ConvertFrom-Json
                    $state | Add-Member -NotePropertyName 'lastCommit' -NotePropertyValue $commitHash -Force
                    $state | Add-Member -NotePropertyName 'lastCommitTime' -NotePropertyValue ([DateTime]::UtcNow.ToString("o")) -Force
                    $state | ConvertTo-Json -Depth 10 | Set-Content -LiteralPath $syncStateFile -Force
                } catch {
                    Write-Verbose "[PostCommit] Failed to update sync state: $_"
                }
            }
        }

        $messages += "Post-commit processing complete"

    } catch {
        # Post-commit is non-blocking, just log errors
        $messages += "Warning during post-commit: $_"
    }

    $duration = ((Get-Date) - $startTime).TotalSeconds

    # Log the hook execution
    Write-GitHookLog -HookType 'post-commit' -Action 'Execute' -Status 'Success' `
        -ProjectRoot $ProjectRoot -Details "Duration: ${duration}s, SyncQueued: $syncQueued"

    return [PSCustomObject]@{
        ExitCode = 0  # Post-commit is always non-blocking
        Success = $true
        Duration = $duration
        SyncQueued = $syncQueued
        Messages = $messages
    }
}

function Invoke-GitHookPrePush {
    <#
    .SYNOPSIS
        Executes pre-push hook logic.

    .DESCRIPTION
        Performs full health check, compatibility validation, and pack validation.
        Fails the push if critical issues are found.

    .PARAMETER ProjectRoot
        Path to the project root. Defaults to current directory.

    .PARAMETER RemoteName
        Name of the remote being pushed to.

    .PARAMETER RemoteUrl
        URL of the remote being pushed to.

    .PARAMETER SkipHealthCheck
        If specified, skips the health check.

    .PARAMETER SkipCompatibilityCheck
        If specified, skips compatibility validation.

    .EXAMPLE
        Invoke-GitHookPrePush -RemoteName 'origin' -RemoteUrl 'https://github.com/user/repo.git'
        Runs pre-push validation.

    .OUTPUTS
        PSCustomObject with ExitCode (0=OK, 1=Block) and validation results.
    #>
    [CmdletBinding()]
    param(
        [Parameter()]
        [string]$ProjectRoot = '.',

        [Parameter()]
        [string]$RemoteName = 'origin',

        [Parameter()]
        [string]$RemoteUrl = '',

        [Parameter()]
        [switch]$SkipHealthCheck,

        [Parameter()]
        [switch]$SkipCompatibilityCheck
    )

    $startTime = Get-Date
    $exitCode = 0
    $findings = @()
    $messages = @()

    # Get current execution mode
    $executionMode = Get-CurrentExecutionMode
    $isCI = ($executionMode -eq 'ci') -or ($env:CI -eq 'true')

    try {
        # Full Health Check
        if (-not $SkipHealthCheck) {
            Write-Verbose "[PrePush] Running full health check..."

            # Check all issue types
            $issueTypes = @('MissingEnvFile', 'InvalidPythonPath', 'MissingBridgeConfig', 
                          'CorruptedBridgeConfig', 'CorruptedSyncState')

            $healthIssues = @()
            foreach ($issueType in $issueTypes) {
                try {
                    $issueCheck = Test-LLMWorkflowIssue -IssueType $issueType -ProjectRoot $ProjectRoot
                    if ($issueCheck.Detected -and $issueCheck.Category -eq 'CRITICAL') {
                        $healthIssues += "$issueType : $($issueCheck.Message)"
                    }
                } catch {
                    Write-Verbose "[PrePush] Failed to check $issueType : $_"
                }
            }

            if ($healthIssues.Count -gt 0) {
                $exitCode = 1
                $findings += @{
                    Check = 'HealthCheck'
                    Status = 'Failed'
                    Details = $healthIssues
                }
                $messages += "CRITICAL: Health check failed with $($healthIssues.Count) critical issues"
            } else {
                $findings += @{
                    Check = 'HealthCheck'
                    Status = 'Passed'
                    Details = $null
                }
            }
        }

        # Compatibility Validation
        if (-not $SkipCompatibilityCheck -and $exitCode -eq 0) {
            Write-Verbose "[PrePush] Checking compatibility..."

            # Check compatibility.lock.json
            $compatPath = Join-Path $ProjectRoot 'compatibility.lock.json'
            if (Test-Path -LiteralPath $compatPath) {
                try {
                    $compat = Get-Content -LiteralPath $compatPath -Raw | ConvertFrom-Json
                    $currentVersion = (Get-LLMWorkflowVersion).Version

                    # Verify version compatibility
                    if ($compat.minVersion -and ([version]$currentVersion -lt [version]$compat.minVersion)) {
                        $exitCode = 1
                        $findings += @{
                            Check = 'Compatibility'
                            Status = 'Failed'
                            Details = "Current version $currentVersion is below minimum required $($compat.minVersion)"
                        }
                        $messages += "CRITICAL: Version compatibility check failed"
                    } else {
                        $findings += @{
                            Check = 'Compatibility'
                            Status = 'Passed'
                            Details = $compat
                        }
                    }
                } catch {
                    $findings += @{
                        Check = 'Compatibility'
                        Status = 'Warning'
                        Details = "Failed to read compatibility.lock.json: $_"
                    }
                }
            } else {
                $findings += @{
                    Check = 'Compatibility'
                    Status = 'Warning'
                    Details = "No compatibility.lock.json found"
                }
            }
        }

        # Pack Validation
        if ($exitCode -eq 0) {
            Write-Verbose "[PrePush] Validating packs..."

            $manifestDir = Join-Path $ProjectRoot 'packs' 'manifests'
            $packIssues = @()

            if (Test-Path -LiteralPath $manifestDir) {
                $manifests = Get-ChildItem -Path $manifestDir -Filter '*.json' -ErrorAction SilentlyContinue
                foreach ($manifest in $manifests) {
                    try {
                        $content = Get-Content -LiteralPath $manifest.FullName -Raw | ConvertFrom-Json -AsHashtable
                        $validation = Test-PackManifest -Manifest $content
                        if (-not $validation.isValid) {
                            $packIssues += "$($manifest.Name): $($validation.errors -join ', ')"
                        }
                    } catch {
                        $packIssues += "$($manifest.Name): Invalid JSON"
                    }
                }
            }

            if ($packIssues.Count -gt 0) {
                $exitCode = 1
                $findings += @{
                    Check = 'PackValidation'
                    Status = 'Failed'
                    Details = $packIssues
                }
                $messages += "CRITICAL: Pack validation failed with $($packIssues.Count) issues"
            } else {
                $findings += @{
                    Check = 'PackValidation'
                    Status = 'Passed'
                    Details = $null
                }
            }
        }

        # Secret Scan (comprehensive on all files to be pushed)
        if ($exitCode -eq 0) {
            Write-Verbose "[PrePush] Scanning for secrets in push..."

            # Get commits being pushed
            $localRef = $env:LOCAL_REF
            $remoteRef = $env:REMOTE_REF

            if ($localRef -and $remoteRef) {
                $commits = & git rev-list "$remoteRef..$localRef" 2>$null
                $secretFound = $false

                foreach ($commit in $commits) {
                    $files = & git diff-tree --no-commit-id --name-only -r $commit 2>$null
                    foreach ($file in $files) {
                        $content = & git show "$commit`:$file" 2>$null
                        if ($content) {
                            $scanResult = Test-SecretInContent -Content $content -SeverityThreshold 'critical'
                            if ($scanResult.HasSecrets) {
                                $criticalCount = ($scanResult.Findings | Where-Object { $_.Severity -eq 'critical' }).Count
                                if ($criticalCount -gt 0) {
                                    $secretFound = $true
                                    break
                                }
                            }
                        }
                    }
                    if ($secretFound) { break }
                }

                if ($secretFound) {
                    $exitCode = 1
                    $findings += @{
                        Check = 'SecretScan'
                        Status = 'Failed'
                        Details = "Critical secrets found in commits to be pushed"
                    }
                    $messages += "CRITICAL: Secrets detected in push commits"
                } else {
                    $findings += @{
                        Check = 'SecretScan'
                        Status = 'Passed'
                        Details = $null
                    }
                }
            }
        }

    } catch {
        $exitCode = 1
        $messages += "Error during pre-push: $_"
        $findings += @{
            Check = 'Error'
            Status = 'Failed'
            Details = $_.Exception.Message
        }
    }

    $duration = ((Get-Date) - $startTime).TotalSeconds

    # Log the hook execution
    Write-GitHookLog -HookType 'pre-push' -Action 'Execute' -Status $(if ($exitCode -eq 0) { 'Success' } else { 'Blocked' }) `
        -ProjectRoot $ProjectRoot -Details "Duration: ${duration}s, Remote: $RemoteName"

    # Output messages
    if ($messages.Count -gt 0) {
        foreach ($msg in $messages) {
            if ($msg -match '^CRITICAL') {
                if ($isCI) {
                    Write-Error $msg
                } else {
                    Write-Host $msg -ForegroundColor Red
                }
            } elseif ($msg -match '^Warning') {
                Write-Warning $msg
            } else {
                Write-Verbose $msg
            }
        }
    }

    return [PSCustomObject]@{
        ExitCode = $exitCode
        Success = ($exitCode -eq 0)
        Duration = $duration
        RemoteName = $RemoteName
        RemoteUrl = $RemoteUrl
        Findings = $findings
        Messages = $messages
    }
}

