function New-GitHookScript {
    <#
    .SYNOPSIS
        Generates Git hook script content.

    .DESCRIPTION
        Creates PowerShell-based hook scripts with cross-platform support
        and proper error handling.

    .PARAMETER HookType
        The type of hook to generate: pre-commit, post-commit, pre-push, post-merge.

    .PARAMETER ProjectRoot
        Path to the project root for resolving relative paths.

    .PARAMETER CustomLogic
        Optional custom PowerShell code to include in the hook.

    .EXAMPLE
        $script = New-GitHookScript -HookType 'pre-commit'
        Generates a pre-commit hook script.

    .OUTPUTS
        String containing the hook script content.
    #>
    [CmdletBinding()]
    [OutputType([string])]
    param(
        [Parameter(Mandatory = $true)]
        [ValidateSet('pre-commit', 'post-commit', 'pre-push', 'post-merge')]
        [string]$HookType,

        [Parameter()]
        [string]$ProjectRoot = '.',

        [Parameter()]
        [string]$CustomLogic = ''
    )

    # Generate header with timestamp
    $header = $script:HookScriptHeader -f ([DateTime]::UtcNow.ToString("o"))

    # Generate hook-specific logic
    $hookLogic = switch ($HookType) {
        'pre-commit' {
            @'
# Pre-commit: Secret scanning and health check
$result = Invoke-GitHookPreCommit -ProjectRoot $PSScriptRoot/.. -StagedFilesOnly
'@
        }
        'post-commit' {
            @'
# Post-commit: Auto-sync (non-blocking)
$result = Invoke-GitHookPostCommit -ProjectRoot $PSScriptRoot/..
'@
        }
        'pre-push' {
            @'
# Pre-push: Full validation
# Read push arguments from stdin
$pushArgs = @()
while ($line = [Console]::In.ReadLine()) {
    if ([string]::IsNullOrWhiteSpace($line)) { break }
    $pushArgs += $line
}

# Parse remote info
$remoteName = $args[0]
$remoteUrl = $args[1]

$result = Invoke-GitHookPrePush -ProjectRoot $PSScriptRoot/.. -RemoteName $remoteName -RemoteUrl $remoteUrl
'@
        }
        'post-merge' {
            @'
# Post-merge: Rebuild affected packs
$isSquash = $args[0] -eq '1'

# Get merge commit info
$mergeCommit = & git rev-parse HEAD
$parents = & git rev-parse HEAD^@ 2>$null

if ($parents.Count -eq 2) {
    # Regular merge - check what changed
    $changedFiles = & git diff-tree --no-commit-id --name-only -r $mergeCommit
    
    # Check for pack changes
    $packChanges = $changedFiles | Where-Object { $_ -like 'packs/*' }
    if ($packChanges) {
        Write-Host "[LLM Workflow] Pack changes detected in merge. Consider rebuilding packs." -ForegroundColor Cyan
    }
}

$result = [PSCustomObject]@{
    ExitCode = 0
    Success = $true
}
'@
        }
        default {
            @'
# Unknown hook type - exit cleanly
$result = [PSCustomObject]@{
    ExitCode = 0
    Success = $true
}
'@
        }
    }

    # Add custom logic if provided
    if ($CustomLogic) {
        $hookLogic += "`n# Custom logic`n$CustomLogic"
    }

    # Combine all parts
    $scriptContent = @($header, $hookLogic, $script:HookScriptFooter) -join "`n"

    return $scriptContent
}

