function Test-GitHookConfiguration {
    <#
    .SYNOPSIS
        Validates the Git hook setup for LLM Workflow.

    .DESCRIPTION
        Checks which hooks are installed, verifies hook scripts are current,
        and reports the complete configuration status.

    .PARAMETER ProjectRoot
        Path to the project root. Defaults to current directory.

    .PARAMETER VerifyContent
        If specified, verifies that installed hooks have valid content.

    .EXAMPLE
        Test-GitHookConfiguration
        Returns the status of all Git hooks for the current project.

    .OUTPUTS
        PSCustomObject with detailed hook configuration status.
    #>
    [CmdletBinding()]
    param(
        [Parameter()]
        [string]$ProjectRoot = '.',

        [Parameter()]
        [switch]$VerifyContent
    )

    # Resolve project root
    $resolvedRoot = Resolve-Path -LiteralPath $ProjectRoot -ErrorAction SilentlyContinue
    if (-not $resolvedRoot) {
        throw "Project root does not exist: $ProjectRoot"
    }
    $resolvedRoot = $resolvedRoot.Path

    $hooksDir = Join-Path $resolvedRoot '.git' 'hooks'
    $hasGitDir = Test-Path -LiteralPath (Join-Path $resolvedRoot '.git')
    $hasHooksDir = Test-Path -LiteralPath $hooksDir

    $hookStatuses = @()
    foreach ($hookType in $script:ValidHookTypes) {
        $hookPath = Join-Path $hooksDir $hookType
        $exists = Test-Path -LiteralPath $hookPath
        $isLLMWorkflowHook = $false
        $isCurrent = $false
        $issues = @()

        if ($exists) {
            $content = Get-Content -LiteralPath $hookPath -Raw -ErrorAction SilentlyContinue
            $isLLMWorkflowHook = $content -and ($content -match 'LLM Workflow Git Hook')

            if ($VerifyContent -and $isLLMWorkflowHook) {
                # Verify the hook has valid content structure
                $isCurrent = ($content -match 'Import-Module LLMWorkflow') -and
                           ($content -match 'Invoke-GitHook')

                if (-not $isCurrent) {
                    $issues += "Hook content may be outdated"
                }
            }

            # Check executable permissions on Unix
            if (-not ($IsWindows -or ($PSVersionTable.PSVersion.Major -lt 6))) {
                $permissions = & stat -c %a $hookPath 2>$null
                if ($permissions -and -not ($permissions -match '^[75]')) {
                    $issues += "Hook may not be executable (permissions: $permissions)"
                }
            }
        }

        $hookStatuses += [PSCustomObject]@{
            HookType = $hookType
            Installed = $exists
            IsLLMWorkflowHook = $isLLMWorkflowHook
            IsCurrent = if ($VerifyContent) { $isCurrent } else { $null }
            Path = if ($exists) { $hookPath } else { $null }
            Issues = $issues
        }
    }

    # Check for global hooks
    $globalHooksPath = & git config --global core.hooksPath 2>$null

    return [PSCustomObject]@{
        ProjectRoot = $resolvedRoot
        HasGitRepository = $hasGitDir
        HasHooksDirectory = $hasHooksDir
        HooksDirectory = if ($hasHooksDir) { $hooksDir } else { $null }
        GlobalHooksPath = $globalHooksPath
        Hooks = $hookStatuses
        Summary = @{
            Total = $script:ValidHookTypes.Count
            Installed = ($hookStatuses | Where-Object { $_.Installed }).Count
            LLMWorkflowHooks = ($hookStatuses | Where-Object { $_.IsLLMWorkflowHook }).Count
            IssuesFound = ($hookStatuses | Where-Object { $_.Issues.Count -gt 0 }).Count
        }
    }
}

